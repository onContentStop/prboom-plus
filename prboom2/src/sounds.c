extern void init_sfx(void) {
  char *name = NULL;
  for (int i = 0; i < sizeof(sfx_names) / sizeof(const char *); ++i) {
    if (!sfx_names[i]) {
      continue;
    }
    vec_init(&S_sfx[i].names);
    name = malloc(strlen(sfx_names[i]) + 1);
    strcpy(name, sfx_names[i]);
    vec_push(&S_sfx[i].names, name);
  }
}

